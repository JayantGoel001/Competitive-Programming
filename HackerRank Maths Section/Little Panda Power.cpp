ll power(ll x,ll y,ll mod)
{
    if(y == 0)
        return 1;
    ll temp = (power(x,y/2,mod))%mod;
    if(y%2 == 1)
        return (temp%mod*temp%mod*x%mod)%mod;
    else    
        return (temp*temp)%mod;
}


ll modInverse(ll a, ll m) 
{ 
    ll m0 = m; 
    ll y = 0, x = 1; 
  
    if (m == 1) 
      return 0; 
  
    while (a > 1) 
    { 
        // q is quotient 
        ll q = a / m; 
        ll t = m; 
  
        // m is remainder now, process same as 
        // Euclid's algo 
        m = a % m, a = t; 
        t = y; 
  
        // Update y and x 
        y = x - q * y; 
        x = t; 
    } 
  
    // Make x positive 
    if (x < 0) 
       x += m0; 
  
    return x; 
} 


